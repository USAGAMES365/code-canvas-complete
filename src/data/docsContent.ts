export const DOCS_PAGES: DocPage[] = Object.entries(docsBlueprint).flatMap(([category, topics]) =>
  topics.map((topic) => ({
    ...topic,
    category,
    sections: autoSections(topic),
  })),
);

export const DOCS_CATEGORIES = Object.entries(
  DOCS_PAGES.reduce<Record<string, DocPage[]>>((acc, page) => {
    acc[page.category] = acc[page.category] ? [...acc[page.category], page] : [page];
    return acc;
  }, {}),
).map(([name, pages]) => ({ name, pages }));

export const TOTAL_DOC_PAGES = DOCS_PAGES.length;
